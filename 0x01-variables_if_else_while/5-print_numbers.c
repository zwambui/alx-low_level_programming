#include <stdio.h>
#include <stdlib.h>
#include <time.h>
/**
 * main - Entry point
 * Description - print all single digit numbers of base 10
 * Return: Always 0
 */
int main(void)
{
	int Number;	
	
	for (Number = 0; Number < 10; Number++);

	printf("%d", Number);
	putchar("\n");

	return (0);





}
